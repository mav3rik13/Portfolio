package com.example.eric.bazaar;

public class uItem {
    private String name;
    private String price;
    private String description;
    private String ID;
    private String user;

    public String getName() {
        return name;
    }

    public void setName(String x) {
        name = x;
    }

    public String getprice() {
        return price;
    }

    public void setprice(String x) {
        price = x;
    }

    public String getdesc() {
        return description;
    }

    public void setdesc(String x) {
        description = x;
    }

    public String getID() {
        return ID;
    }

    public void setID(String x) {
        ID = x;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String x) {
        user = x;
    }

}
